XMIN  = 469490.0
YMIN  = 2934890.0
EPSG  = 32643
LEVELS = [270, 295, 320, 345, 370]
CS_G  = 5.0   # gravity pixel size (m)
CS_M  = 30.0  # magnetics pixel size (m)


def rewrite_folder(src_grav_dir, src_mag_dir, dst_grav_dir, dst_mag_dir,
                   label: str = '') -> None:
    """Read PIL TIFs from src, write proper GeoTIFFs to dst."""
    os.makedirs(dst_grav_dir, exist_ok=True)
    os.makedirs(dst_mag_dir,  exist_ok=True)

    print(f"\n{label}")
    for mrl in LEVELS:
        # Gravity
        g_src = os.path.join(src_grav_dir, f'grav_{mrl}.tif')
        g_dst = os.path.join(dst_grav_dir, f'grav_{mrl}.tif')
        arr_g = read_float_tif(g_src)   # south-up as generated
        write_geotiff_float32(arr_g, g_dst, XMIN, YMIN, CS_G, CS_G, EPSG)
        print(f"  grav_{mrl}.tif  {arr_g.shape}  "
              f"min={arr_g.min():.3f}  max={arr_g.max():.3f} mGal  -> {g_dst}")

        # Magnetics
        m_src = os.path.join(src_mag_dir, f'mag_{mrl}.tif')
        m_dst = os.path.join(dst_mag_dir, f'mag_{mrl}.tif')
        arr_m = read_float_tif(m_src)   # south-up as generated
        write_geotiff_float32(arr_m, m_dst, XMIN, YMIN, CS_M, CS_M, EPSG)
        print(f"  mag_{mrl}.tif   {arr_m.shape}  "
              f"min={arr_m.min():.2f}  max={arr_m.max():.2f} uSI  -> {m_dst}")


